package Day04;

public class PostiveOrNegativeNumber {

	public static void main(String[] args) {

		int num=20;
		
		if(num>0)
		{
			System.out.println("Postive No");

		}
		else if (num<0) {
			System.out.println("Negative No");

		}
		else {
			System.out.println("Zero No");

		}
		
	}

}


